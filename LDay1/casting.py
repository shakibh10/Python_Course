# Integer Type Casting 


x=int(1)
y=int(2.5)
z=int("3") # This string can not be float value 

print(x)
print(y)
print(z)


# Float type casting 

a=float(1)
b=float(1.6)
c=float("4")
d=float("4.5")

print(a)
print(b)
print(c)
print(d)


# String type casting 

p=str("SAH75")
q=str(6)
r=str(8.6)

print(p)
print(q)
print(r)
